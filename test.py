x = int(input('Input the first number: '))
y = int(input('Input the second number: '))
if x > y:
	print('x is greater than y')
elif x == y:
	print('x is equal to y')
else:
	print('x is smaller than y')

if x > 0:
	print('x is a positive number')
elif x == 0:
	print('x is a zero')
else:
	print('x is a positive number')
